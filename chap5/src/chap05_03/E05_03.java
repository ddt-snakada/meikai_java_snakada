/*論理型の変数にtrueやfalseを代入してその値を表示するプログラムを作成せよ*/
package chap05_03;

import java.util.Scanner;

public class E05_03 {
	public static void main(String[] args) {		
		//論理型にfalseをいれて宣言
		boolean boolFalse = false;
		//論理型にfalseをいれて宣言
		boolean boolTrue = true;
		
		//falseを表示する
		System.out.println(boolFalse);
		//trueを表示する
		System.out.println(boolTrue);
	}

}
